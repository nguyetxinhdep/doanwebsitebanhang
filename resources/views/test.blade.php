<script src="https://code.jquery.com/jquery-3.6.0.min.js"></script>
<script>
    $(document).ready(function() {
        $('#search-input').on('keyup', function() {
            var query = $(this).val();

            if (query.length > 0) {
                $.ajax({
                    url: "{{ route('search') }}",
                    type: "GET",
                    data: { query: query },

                    success: function(data) {
                        var results = $('#search-results');
                        results.empty();//xóa các phần tử bên trong thẻ đã hiện trước đó

                        if (data.length > 0) {
                            data.forEach(function(item) {
                                results.append('<div class="px-4 py-2 hover:bg-gray-100 cursor-pointer">' + item.name + '</div>');
                            });
                        } else {
                            results.append('<div class="px-4 py-2 text-gray-500">Không tìm thấy kết quả</div>');
                        }
                    }
                });
            } else {
                $('#search-results').empty();
            }
        });
    });
</script>
